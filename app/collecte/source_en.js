/** Traduit une PHRASE française en anglais (idiome exact, sinon règle de pattern). null sinon. */
function phraseEn(s) {
  let low = s.trim().toLowerCase();
  if (Object.prototype.hasOwnProperty.call(IDIOM, low)) return IDIOM[low];
  const isQ = low.endsWith("?");
  const body = isQ ? low.replace(/\s*\?\s*$/, "") : low;   // « peux-tu manger ? » → « peux-tu manger »
  for (const [prefix, map, tpl, question] of RULES) {
    if (question && !isQ) continue;
    const src = question ? body : low;
    if (src.startsWith(prefix)) {
      const arg = src.slice(prefix.length).trim();
      if (Object.prototype.hasOwnProperty.call(map, arg)) return cap(tpl(map[arg]));
    }
  }
  return null;
}

/** Équivalent ANGLAIS d'un item source français (lettre, chiffre, mot OU phrase), ou null. */
export function sourceEn(fr) {
  if (fr == null) return null;
  const s = String(fr).trim();
  if (!s) return null;
  if (/^[A-Za-z]$/.test(s)) return s.toUpperCase();          // lettres isolées : identiques
  if (!/\p{L}/u.test(s)) return s;                            // chiffres/ponctuation seuls : identiques
  const k = s.toLowerCase();
  if (Object.prototype.hasOwnProperty.call(DICT, k)) return DICT[k];   // nombres + mots
  return phraseEn(s);                                        // phrases (idiomes + patterns)
}

