//
// Auteur : Brice Kengni Zanguim.

const LS_UI = "langa-ui";

export function getUiLang() {
  const v = localStorage.getItem(LS_UI);
  return v === "en" ? "en" : "fr";
}
export function setUiLang(l) {
  localStorage.setItem(LS_UI, l === "en" ? "en" : "fr");
}

